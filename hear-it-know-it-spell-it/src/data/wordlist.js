/** Returns all words for a given grade, sorted by round then position. */
export function getWordsForGrade(grade) {
  return WORDS.filter((w) => w.grade === grade).sort((a, b) =>
    a.round !== b.round ? a.round - b.round : 0
  );
}

/** Returns words for a specific grade and round (5 words). */
export function getWordsForRound(grade, round) {
  return WORDS.filter((w) => w.grade === grade && w.round === round);
}
